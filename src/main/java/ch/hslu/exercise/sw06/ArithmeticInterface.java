package ch.hslu.exercise.sw06;

/**
 * Interface for basic arithmetic methods
 *
 */

public interface ArithmeticInterface {

    /**
     * This method combines two numbers and produces a combined number.
     * @param firstParameter The first parameter to combine.
     * @param secondParameter The seconds parameter to combine.
     * @return Sum of both parameters
     */
    public float addition(Float firstParameter, Float secondParameter);
}
